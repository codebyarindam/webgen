<?php

// Assuming groups data is stored in a database
$groups = [
    ['id' => 1, 'name' => 'Group 1'],
    ['id' => 2, 'name' => 'Group 2'],
    // Add more groups here...
];

// Function to display groups
function displayGroups($groups) {
    echo "<h1>Existing Groups:</h1>";
    echo "<ul>";
    foreach ($groups as $group) {
        echo "<li>";
        echo "<a href='?group_id={$group['id']}'>{$group['name']}</a>";
        echo "</li>";
    }
    echo "</ul>";
}

// Function to create a new group
function createGroup() {
    echo "<h2>Create a new group:</h2>";
    echo "<form action='' method='post'>";
    echo "<input type='text' name='group_name' placeholder='Group Name'>";
    echo "<input type='submit' value='Create'>";
    echo "</form>";
}

// Function to view/edit contacts of a group
function viewEditContacts($groupId) {
    // Assuming contacts data is stored in a database
    $contacts = [
        ['id' => 1, 'name' => 'Contact 1', 'group_id' => 1],
        ['id' => 2, 'name' => 'Contact 2', 'group_id' => 1],
        // Add more contacts here...
    ];

    $groupContacts = array_filter($contacts, function($contact) use ($groupId) {
        return $contact['group_id'] == $groupId;
    });

    if (empty($groupContacts)) {
        echo "<p>No contacts found in this group.</p>";
    } else {
        echo "<h2>Contacts in Group {$groupId}:</h2>";
        echo "<ul>";
        foreach ($groupContacts as $contact) {
            echo "<li>";
            echo "<a href='?contact_id={$contact['id']}'>{$contact['name']}</a>";
            echo "</li>";
        }
        echo "</ul>";
    }
}

// Main logic
if (isset($_GET['group_id'])) {
    viewEditContacts($_GET['group_id']);
} else {
    displayGroups($groups);
    createGroup();
}

?>
